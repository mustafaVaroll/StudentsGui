    public void ogrenciGoruntule() throws FileNotFoundException {
        
        model.setRowCount(0);
        
        ArrayList<Ogrenci> ogrenciler = new ArrayList<Ogrenci>();
        
        
        ogrenciler = islemler.cikti;
        
        if (ogrenciler != null ) {
            
            for (Ogrenci ogrenci : ogrenciler) {
                Object[] eklenecek = {ogrenci.getNo(),ogrenci.getAd(),ogrenci.getSoyad(),ogrenci.getKredi(),ogrenci.getGpa()};
                
                model.addRow(eklenecek);
        
            }
            
        }
     
    }
    
